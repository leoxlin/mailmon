from typing import Annotated, Optional

import typer
from dotenv.main import load_dotenv
from rich import print as rprint

from mailmon.planner import Planner

# Load .env into os.environ before imports
load_dotenv()

from mailmon.llm import Classifier
from mailmon.mailbox import (
    format_addresses,
    get_email,
    get_emails,
    get_mailbox,
    get_mailboxes,
)

app = typer.Typer()


@app.command(help="Debug prompts generated by mailmon for the LLM classifier")
def prompt(email_id: Optional[str] = None):
    mailboxes = get_mailboxes()
    classifier = Classifier(mailboxes)
    rprint(":robot: [bold green]System Prompt:[/bold green]\n")
    print(classifier.system_prompt())

    if email_id:
        email = get_email(email_id)
        print()
        rprint(":man_medium_skin_tone: [bold green]User Prompt:[/bold green]\n")
        print(classifier.user_prompt(email))


@app.command(help="Generate a plan for how we will classify emails")
def plan(
    email_id: Annotated[
        Optional[str],
        typer.Option(
            "--email", help="Run plan for a specific email id", metavar="EMAIL_ID"
        ),
    ] = None,
    regenerate: Annotated[
        bool, typer.Option(help="Regenerate any persisted plans")
    ] = False,
):
    emails = []
    if email_id:
        emails = [get_email(email_id)]
    else:
        inbox = get_mailbox("Inbox")
        emails = get_emails(inbox)
    planner = Planner()
    for email in emails:
        plan = planner.plan(email, regenerate)
        print(f"{format_addresses(email.mail_from)}:", email.subject)
        planner.print(plan)
        print()


